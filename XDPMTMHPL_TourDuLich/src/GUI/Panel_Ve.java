    void showKhachHang() {
        khachhang = new KhachHangDAOImp().loadDataKhachHang();
        modelkh.setRowCount(0);
        for (KhachHangDTO kh : khachhang) {
            modelkh.addRow(new Object[]{
                kh.getMakh(), kh.getTenkh()
            });
        }
    }

